void main() {
  // Integer variable
  int age = 25;
  print('Age: $age');

  // Double variable
  double height = 5.9;
  print('Height: $height');

  // String variable
  String name = 'John Doe';
  print('Name: $name');

  // Boolean variable
  bool isStudent = true;
  print('Is Student: $isStudent');

  // Dynamic variable (can hold any type of value)
  dynamic variable = 'Hello';
  print('Dynamic variable: $variable');
  variable = 123;
  print('Dynamic variable: $variable');
}
